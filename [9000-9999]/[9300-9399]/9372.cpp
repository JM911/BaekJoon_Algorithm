#include <bits/stdc++.h>
using namespace std;

int T, N, M;

int main()
{
    cin.tie(nullptr);
    ios_base::sync_with_stdio(false);

    cin >> T;

    while(T--)
    {
        cin >> N >> M;

        while(M--)
        {
            int a, b;
            cin >> a >> b;
        }

        cout << N - 1 << '\n';
    }
}